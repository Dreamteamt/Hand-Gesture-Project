import baha as best
import cv2
import mediapipe as mp
import sys
import time
from PyQt5.QtWidgets import QApplication, QLabel, QVBoxLayout, QWidget
from PyQt5.QtGui import QImage, QPixmap
from PyQt5.QtCore import QTimer, Qt

class koena(QWidget):
    def __init__(self):
        super().__init__()
        self.setupUI()
        self.launchCam()
        self.stopTimer=None

    def setupUI(self):
        self.setWindowTitle("Hand Gesture Recognition")
        self.setGeometry(100,100,800,600)
        self.text=QLabel("Recognized Gesture: ",self)
        self.text.setAlignment(Qt.AlignCenter)
        self.text.setStyleSheet("font-size:48px;color:white;background-color:black;")
        self.image=QLabel(self)
        self.image.setAlignment(Qt.AlignCenter)
        layout=QVBoxLayout()
        layout.addWidget(self.text,1)
        layout.addWidget(self.image,2)
        self.setLayout(layout)

    def launchCam(self):
        self.cam=cv2.VideoCapture(0)
        if not self.cam.isOpened():
            print("Error: Could not open webcam.")
            sys.exit(1)
        self.hands=mp.solutions.hands
        self.draw=mp.solutions.drawing_utils
        self.processHands=self.hands.Hands(min_detection_confidence=0.8,min_tracking_confidence=0.8)
        self.timer=QTimer()
        self.timer.timeout.connect(self.processFrame)
        self.timer.start(30)

    def thumbCheck(self,hand):
        thumbTip=hand.landmark[self.hands.HandLandmark.THUMB_TIP]
        thumbMCP=hand.landmark[self.hands.HandLandmark.THUMB_MCP]
        return thumbTip.y<thumbMCP.y if thumbTip.y<thumbMCP.y else thumbTip.x>thumbMCP.x

    def getGesture(self,hand):
        fingers=[8,12,16,20]
        thumbTip,thumbBase=4,1
        states=[1 if hand.landmark[f].y<hand.landmark[f-2].y else 0 for f in fingers]
        thumbUp=hand.landmark[thumbTip].y<hand.landmark[thumbBase].y
        thumbDown=hand.landmark[thumbTip].y>hand.landmark[thumbBase].y
        thumb=1 if self.thumbCheck(hand) else 0
        if thumbUp and sum(states)==0:
            return "Like üëç"
        if thumbDown and sum(states)==0:
            return "Dislike üëé"
        if states==[0,0,0,1] and thumb:
            return "abayudna ü§ô"
        if sum(states)==4:
            return "Stop ‚úã"
        if states==[1,0,0,0]:
            return "One Finger Up ‚òù"
        if states==[1,1,0,0]:
            return "Victory ‚úå"
        if states==[1,0,0,1]:
            return "Rock ü§ò"
        if abs(hand.landmark[4].x-hand.landmark[8].x)<0.02:
            return "OK üëå"
        return "Unknown Gesture"

    def processFrame(self):
        ret,frame=self.cam.read()
        if not ret:
            return
        frame=cv2.flip(frame,1)
        rgbFrame=cv2.cvtColor(frame,cv2.COLOR_BGR2RGB)
        result=self.processHands.process(rgbFrame)
        gesture="Recognizing..."
        if result.multi_hand_landmarks:
            for hand in result.multi_hand_landmarks:
                self.draw.draw_landmarks(frame,hand,self.hands.HAND_CONNECTIONS)
                recognized=self.getGesture(hand)
                if recognized=="Stop ‚úã":
                    if self.stopTimer is None:
                        self.stopTimer=time.time()
                    elif time.time()-self.stopTimer>=3:
                        self.close()
                        return
                else:
                    self.stopTimer=None
                gesture=recognized
        self.text.setText(f"Recognized Gesture: {gesture}")
        finalFrame=cv2.cvtColor(frame,cv2.COLOR_BGR2RGB)
        h,w,ch=finalFrame.shape
        bytesPerLine=ch*w
        q_img=QImage(finalFrame.data,w,h,bytesPerLine,QImage.Format_RGB888)
        self.image.setPixmap(QPixmap.fromImage(q_img))

    def closeEvent(self,event):
        if self.cam.isOpened():
            self.cam.release()
        self.processHands.close()
        cv2.destroyAllWindows()
        event.accept()

if __name__=="__main__":
    app=QApplication(sys.argv)
    window=koena()
    window.showFullScreen()
    sys.exit(app.exec_())
